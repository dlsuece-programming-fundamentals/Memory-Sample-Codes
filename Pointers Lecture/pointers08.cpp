#include <stdio.h>

int main(){
	int arr[3] = {2, 4, 8};
	
	printf("arr[0] = %d\n", arr[0]);	//this prints the value of the first element
	printf("arr[1] = %d\n", arr[1]);	//this prints the value of the second element
	printf("arr[2] = %d\n", arr[2]);	//this prints the value of the third element
	printf("arr = %x\n", arr);			//this prints the address of the first element
	
	return 0;
}
